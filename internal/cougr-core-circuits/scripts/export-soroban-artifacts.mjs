#!/usr/bin/env bun
/**
 * Convert snarkjs VK / proof / public JSON into Soroban BN254 byte layouts and
 * emit Rust constants for cougr-core on-chain verification tests.
 *
 * G1: be_bytes(x) || be_bytes(y)
 * G2: be_bytes(x.c1) || be_bytes(x.c0) || be_bytes(y.c1) || be_bytes(y.c0)
 * Fr public inputs: 32-byte big-endian field elements
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const EXPORTED = join(ROOT, "exported");
const WORK = join(ROOT, "work");
const OUT_VK = join(ROOT, "..", "..", "src", "circuits", "generated.rs");
const OUT_FIXTURES = join(ROOT, "..", "..", "src", "circuits", "generated_fixtures.rs");

const CIRCUITS = ["hidden_cards", "fog_of_war", "fair_dice", "sealed_bid"];

const FR_MODULUS =
  0x30644e72e131a029b85045b68181585d2833e84879b9709143e1f593f0000001n;

function toBigInt(v) {
  if (typeof v === "bigint") return v;
  return BigInt(v);
}

function fpToBe32(n) {
  let x = toBigInt(n);
  if (x < 0n) x = ((x % FR_MODULUS) + FR_MODULUS) % FR_MODULUS;
  const out = new Uint8Array(32);
  for (let i = 31; i >= 0; i--) {
    out[i] = Number(x & 0xffn);
    x >>= 8n;
  }
  return out;
}

function g1ToBytes(point) {
  const out = new Uint8Array(64);
  out.set(fpToBe32(point[0]), 0);
  out.set(fpToBe32(point[1]), 32);
  return out;
}

/** snarkjs stores Fq2 as [c0, c1]; Soroban expects c1 || c0 per coordinate. */
function g2ToBytes(point) {
  const out = new Uint8Array(128);
  out.set(fpToBe32(point[0][1]), 0);
  out.set(fpToBe32(point[0][0]), 32);
  out.set(fpToBe32(point[1][1]), 64);
  out.set(fpToBe32(point[1][0]), 96);
  return out;
}

function proofToBytes(proof) {
  return {
    a: g1ToBytes(proof.pi_a),
    b: g2ToBytes(proof.pi_b),
    c: g1ToBytes(proof.pi_c),
  };
}

function vkToParts(vk) {
  const ic = vk.IC.map((p) => g1ToBytes(p));
  return {
    alpha: g1ToBytes(vk.vk_alpha_1),
    beta: g2ToBytes(vk.vk_beta_2),
    gamma: g2ToBytes(vk.vk_gamma_2),
    delta: g2ToBytes(vk.vk_delta_2),
    ic,
    nPublic: vk.nPublic,
  };
}

function bytesToRustArray(bytes, name) {
  const hex = [...bytes].map((b) => `0x${b.toString(16).padStart(2, "0")}`).join(", ");
  return `pub const ${name}: [u8; ${bytes.length}] = [${hex}];`;
}

function bytes2dToRust(rows, name, rowLen, comment = "row") {
  const inner = rows
    .map((row, i) => {
      const hex = [...row].map((b) => `0x${b.toString(16).padStart(2, "0")}`).join(", ");
      return `    [${hex}], // ${comment}[${i}]`;
    })
    .join("\n");
  return `pub const ${name}: [[u8; ${rowLen}]; ${rows.length}] = [\n${inner}\n];`;
}

function upper(name) {
  return name.toUpperCase();
}

function loadJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

const vkHeader = [
  "// @generated by internal/cougr-core-circuits/scripts/export-soroban-artifacts.mjs",
  "// Run `bun run pipeline` to refresh after circuit or fixture changes.",
  "",
  "//! Pipeline verification keys for `cougr_core::circuits`.",
  "//! Development-only — never ship CI-generated VKs to production mainnet.",
  "",
];

const fixtureHeader = [
  "// @generated by internal/cougr-core-circuits/scripts/export-soroban-artifacts.mjs",
  "// Run `bun run pipeline` to refresh after circuit or fixture changes.",
  "",
  "//! Pipeline proofs and public inputs for integration tests.",
  "",
];

const vkLines = [...vkHeader];
const fixtureLines = [...fixtureHeader];

for (const circuit of CIRCUITS) {
  const vkPath = join(EXPORTED, `${circuit}_vk.json`);
  const proofPath = join(WORK, circuit, "proof.json");
  const publicPath = join(WORK, circuit, "public.json");

  if (!existsSync(vkPath)) {
    console.error(`missing ${vkPath} — run setup/export-vk first`);
    process.exit(1);
  }
  if (!existsSync(proofPath) || !existsSync(publicPath)) {
    console.error(`missing proof/public for ${circuit} — run prove.sh first`);
    process.exit(1);
  }

  const vk = loadJson(vkPath);
  const proof = loadJson(proofPath);
  const publicSignals = loadJson(publicPath);

  const vkParts = vkToParts(vk);
  const proofParts = proofToBytes(proof);
  const publicBytes = publicSignals.map((s) => fpToBe32(s));

  const prefix = upper(circuit);
  vkLines.push(`// ── ${circuit} ──`);
  vkLines.push(bytesToRustArray(vkParts.alpha, `${prefix}_VK_ALPHA`));
  vkLines.push(bytesToRustArray(vkParts.beta, `${prefix}_VK_BETA`));
  vkLines.push(bytesToRustArray(vkParts.gamma, `${prefix}_VK_GAMMA`));
  vkLines.push(bytesToRustArray(vkParts.delta, `${prefix}_VK_DELTA`));
  vkLines.push(bytes2dToRust(vkParts.ic, `${prefix}_VK_IC`, 64, "ic"));
  vkLines.push(`pub const ${prefix}_VK_N_PUBLIC: u32 = ${vkParts.nPublic};`);
  vkLines.push("");

  fixtureLines.push(`// ── ${circuit} ──`);
  fixtureLines.push(bytesToRustArray(proofParts.a, `${prefix}_PROOF_A`));
  fixtureLines.push(bytesToRustArray(proofParts.b, `${prefix}_PROOF_B`));
  fixtureLines.push(bytesToRustArray(proofParts.c, `${prefix}_PROOF_C`));
  fixtureLines.push(bytes2dToRust(publicBytes, `${prefix}_PUBLIC_INPUTS`, 32, "public"));
  fixtureLines.push("");
}

mkdirSync(dirname(OUT_VK), { recursive: true });
writeFileSync(OUT_VK, vkLines.join("\n") + "\n");
writeFileSync(OUT_FIXTURES, fixtureLines.join("\n") + "\n");
console.log("wrote", OUT_VK);
console.log("wrote", OUT_FIXTURES);